#!/usr/bin/env node
const fs = require('fs')
const path = require('path')
const fetch = globalThis.fetch || require('node-fetch')

async function run() {
  const owner = process.env.GITHUB_OWNER || process.env.NEXT_PUBLIC_GITHUB_USERNAME || process.argv[2] || process.env.GITHUB_REPOSITORY?.split('/')?.[0]
  if (!owner) {
    console.error('No GitHub owner specified. Set GITHUB_OWNER env or pass as first arg.')
    process.exit(1)
  }
  const token = process.env.GITHUB_TOKEN
  const headers = { Accept: 'application/vnd.github.v3+json' }
  if (token) headers.Authorization = `token ${token}`

  console.log('Fetching repos for', owner)
  const res = await fetch(`https://api.github.com/users/${owner}/repos?per_page=100&sort=updated`, { headers })
  if (!res.ok) {
    console.error('Failed fetching repos:', res.status, res.statusText)
    process.exit(1)
  }

  const repos = (await res.json()).filter(r => {
    if (r.fork || r.archived) return false
    return String(r.name).toLowerCase() !== String(owner).toLowerCase()
  })

  const out = []

  for (const r of repos) {
    try {
      const repoName = r.name
      const repoUrl = r.html_url
      const desc = r.description || ''
      const updated = r.updated_at

      // fetch languages
      let tech = []
      try {
        const langRes = await fetch(`https://api.github.com/repos/${owner}/${repoName}/languages`, { headers })
        if (langRes.ok) {
          const langObj = await langRes.json()
          tech = Object.keys(langObj).sort((a,b) => (langObj[b]||0)-(langObj[a]||0)).slice(0,5).map(x => String(x))
        }
      } catch (e) {}

      // fetch README first paragraph as fallback description
      let readmeText = ''
      try {
        const readmeRes = await fetch(`https://api.github.com/repos/${owner}/${repoName}/readme`, { headers: { ...headers, Accept: 'application/vnd.github.v3.raw' } })
        if (readmeRes.ok) {
          readmeText = await readmeRes.text()
          // sanitize markdown: remove headings and code blocks
          readmeText = readmeText.replace(/```[\s\S]*?```/g, '')
          readmeText = readmeText.replace(/#.*\n/g, '')
          // find first non-empty paragraph
          const paras = readmeText.split(/\n\s*\n/).map(p => p.replace(/\n/g, ' ').trim()).filter(Boolean)
          if (paras.length > 0 && !desc) {
            // use first paragraph under 300 chars
            let candidate = paras[0].slice(0, 600)
            candidate = candidate.split('\n').join(' ')
            readmeText = candidate
          }
        }
      } catch (e) {}

      const shortDescription = desc || (readmeText && readmeText.slice(0, 280)) || 'No description available.'
      const fullDescription = readmeText || ''

      out.push({
        id: repoName,
        title: repoName,
        shortDescription,
        fullDescription,
        tech,
        github: repoUrl,
        lastUpdated: updated,
        autoGenerated: true,
        generatedFrom: 'github',
        badge: 'Auto-generated from GitHub',
        category: 'GitHub',
      })
    } catch (err) {
      console.warn('Skipping repo', r.name, err && err.message)
    }
  }

  // write to lib/generated_projects.ts
  const outPath = path.join(__dirname, '..', 'lib', 'generated_projects.ts')
  const header = "import type { Project } from './data'\n\n// Auto-generated file. Do not edit by hand.\n"
  const fileContent = header + 'export const GENERATED_PROJECTS: Project[] = ' + JSON.stringify(out, null, 2) + '\n'

  fs.writeFileSync(outPath, fileContent, 'utf8')
  console.log('Wrote', out.length, 'projects to', outPath)
}

run().catch(err => { console.error(err); process.exit(1) })
